package eu.elixir.ega.ebi.keyproviderservice.rest;

import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

import javax.management.Attribute;
import javax.management.AttributeList;
import javax.management.MBeanServer;
import javax.management.ObjectName;
import java.lang.management.ManagementFactory;

/**
 * @author asenf
 */
@RestController
@RequestMapping("/stats")
public class StatsController {

    // Obtain local CPU Load (used by EBI Load Balancer as Heartbeat)
    private static double getProcessCpuLoad() throws Exception {
        MBeanServer mbs = ManagementFactory.getPlatformMBeanServer();
        ObjectName name = ObjectName.getInstance("java.lang:type=OperatingSystem");
        AttributeList list = mbs.getAttributes(name, new String[]{"ProcessCpuLoad"});

        if (list.isEmpty()) {
            return Double.NaN;
        }

        Attribute att = (Attribute) list.get(0);
        Double value = (Double) att.getValue();

        if (value == -1.0) {
            return Double.NaN;
        }
        return ((int) (value * 1000) / 10.0);
    }

    @GetMapping(value = "/load")
    public String get() {
        String load;
        try {
            load = String.valueOf(getProcessCpuLoad());
        } catch (Exception ex) {
            load = "Error";
        }

        return load;
    }

}
